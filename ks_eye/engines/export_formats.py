def export_to_markdown(content, title="Research Report", metadata=None):
    """Convert research content to well-formatted Markdown"""
    lines = []
    
    # Front matter
    lines.append("---")
    lines.append(f"title: '{title}'")
    lines.append(f"date: {datetime.now().strftime('%Y-%m-%d')}")
    if metadata:
        for key, value in metadata.items():
            lines.append(f"{key}: '{value}'")
    lines.append("---")
    lines.append("")
    
    # Title
    lines.append(f"# {title}")
    lines.append("")
    
    # Convert content - look for headings and format them
    paragraphs = content.split("\n")
    in_list = False
    
    for line in paragraphs:
        line = line.strip()
        if not line:
            if in_list:
                in_list = False
            lines.append("")
            continue
        
        # Check for ALL CAPS headings
        if line.isupper() and len(line) < 80 and len(line) > 3:
            lines.append(f"## {line.title()}")
            lines.append("")
        # Check for numbered sections
        elif re.match(r'^\d+[\.\)]\s+[A-Z]', line):
            lines.append(f"## {line}")
            lines.append("")
        # Check for bullet points
        elif line.startswith("-") or line.startswith("•"):
            if not in_list:
                lines.append("")
                in_list = True
            lines.append(f"  {line}")
        # Regular paragraph
        else:
            if in_list:
                lines.append("")
                in_list = False
            # Bold text in ALL CAPS within sentences
            line = re.sub(r'\*\*([^*]+)\*\*', r'**\1**', line)
            lines.append(line)
            lines.append("")
    
    # Footer
    lines.append("")
    lines.append("---")
    lines.append(f"*Generated by ks-eye Research Assistant on {datetime.now().strftime('%Y-%m-%d %H:%M')}*")
    
    return "\n".join(lines)


def export_to_csv(data_points, filename=None):
    """Export data points to CSV format"""
    if not data_points:
        return None, "No data to export"
    
    output = io.StringIO()
    
    # Determine columns from first data point
    if isinstance(data_points[0], dict):
        fieldnames = list(data_points[0].keys())
        writer = csv.DictWriter(output, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(data_points)
    else:
        writer = csv.writer(output)
        for row in data_points:
            writer.writerow(row if isinstance(row, (list, tuple)) else [row])
    
    csv_content = output.getvalue()
    
    if not filename:
        ts = datetime.now().strftime("%Y%m%d_%H%M%S")
        filename = os.path.join(config.RESEARCH_DIR, f"data_export_{ts}.csv")
    
    os.makedirs(os.path.dirname(filename), exist_ok=True)
    with open(filename, "w") as f:
        f.write(csv_content)
    
    return filename, csv_content


def generate_email_newsletter(content, title="Research Update", max_length=2000):
    """Generate an email newsletter format from research"""
    lines = []
    
    # Email header
    lines.append(f"Subject: {title}")
    lines.append(f"From: research@kseye.ai")
    lines.append(f"Date: {datetime.now().strftime('%B %d, %Y')}")
    lines.append("")
    lines.append("=" * 60)
    lines.append("")
    
    # Preheader
    lines.append(f"[PREVIEW] Key findings from our latest research on {title}")
    lines.append("")
    
    # Body
    lines.append("Hi there,")
    lines.append("")
    lines.append(f"We've completed new research on **{title}**. Here are the key findings:")
    lines.append("")
    
    # Extract key points
    sentences = content.replace("\n", " ").split(".")
    key_points = [s.strip() for s in sentences[:8] if len(s.strip()) > 50]
    
    for i, point in enumerate(key_points[:5], 1):
        lines.append(f"{i}. {point}.")
        lines.append("")
    
    # Read more section
    lines.append("")
    lines.append("=" * 60)
    lines.append("READ THE FULL REPORT")
    lines.append("=" * 60)
    lines.append("")
    
    # Include excerpt
    excerpt = content[:max_length]
    lines.append(excerpt)
    
    if len(content) > max_length:
        lines.append("")
        lines.append("[... Read full report for complete analysis ...]")
    
    # Footer
    lines.append("")
    lines.append("=" * 60)
    lines.append("Generated by ks-eye Research Assistant")
    lines.append(f"Date: {datetime.now().strftime('%Y-%m-%d')}")
    lines.append("=" * 60)
    
    return "\n".join(lines)


