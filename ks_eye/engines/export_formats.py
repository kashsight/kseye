def export_to_notion_obsidian(content, title="Research", format_type="notion"):
    """
    Export to Notion or Obsidian format
    Both use markdown but with different conventions
    """
    lines = []
    
    if format_type == "notion":
        # Notion-style markdown
        lines.append(f"# {title}")
        lines.append("")
        
        # Notion callouts
        lines.append(f"> 💡 Research on {title}")
        lines.append(f"> Generated: {datetime.now().strftime('%Y-%m-%d')}")
        lines.append("")
        
        # Convert content
        paragraphs = content.split("\n\n")
        for para in paragraphs[:20]:
            if para.strip().isupper() and len(para.strip()) < 80:
                lines.append(f"## {para.strip().title()}")
                lines.append("")
            else:
                lines.append(para.strip())
                lines.append("")
    
    elif format_type == "obsidian":
        # Obsidian-style markdown with wikilinks
        lines.append(f"# {title}")
        lines.append("")
        
        # Tags
        lines.append("Tags: #research #analysis #ks-eye")
        lines.append(f"Date: {datetime.now().strftime('%Y-%m-%d')}")
        lines.append("")
        
        # Convert content with wikilinks for key concepts
        import re
        concept_pattern = re.compile(r'\b[A-Z][a-z]+(?:\s+[A-Z][a-z]+)*\b')
        
        paragraphs = content.split("\n\n")
        for para in paragraphs[:20]:
            # Add wikilinks to concepts
            para_with_links = para
            for match in concept_pattern.finditer(para):
                concept = match.group()
                if len(concept) > 4:
                    para_with_links = para_with_links.replace(concept, f"[[{concept}]]", 1)
            
            if para.strip().isupper() and len(para.strip()) < 80:
                lines.append(f"## {para.strip().title()}")
                lines.append("")
            else:
                lines.append(para_with_links.strip())
                lines.append("")
    
    return "\n".join(lines)


def save_export(content, export_type, topic, folder=None):
    """Save export to file and return path"""
    if not folder:
        ts = datetime.now().strftime("%Y%m%d_%H%M%S")
        safe_topic = "".join(c if c.isalnum() else "_" for c in topic)[:40]
        folder = os.path.join(config.RESEARCH_DIR, f"exports_{safe_topic}_{ts}")
    
    os.makedirs(folder, exist_ok=True)
    
    if export_type == "markdown":
        filename = os.path.join(folder, f"{topic[:30].replace(' ', '_')}.md")
    elif export_type == "csv":
        filename = os.path.join(folder, f"{topic[:30].replace(' ', '_')}.csv")
    elif export_type == "newsletter":
        filename = os.path.join(folder, f"newsletter_{topic[:30].replace(' ', '_')}.txt")
    elif export_type == "readme":
        filename = os.path.join(folder, "README.md")
    elif export_type == "notion":
        filename = os.path.join(folder, f"{topic[:30].replace(' ', '_')}_notion.md")
    elif export_type == "obsidian":
        filename = os.path.join(folder, f"{topic[:30].replace(' ', '_')}_obsidian.md")
    else:
        filename = os.path.join(folder, f"export_{topic[:30].replace(' ', '_')}.txt")
    
    with open(filename, "w") as f:
        f.write(content)
    
    return filename, folder


def generate_all_exports(content, topic, folder=None):
    """Generate all export formats and return paths"""
    exports = {}
    
    # Markdown
    md_content = export_to_markdown(content, topic)
    md_path, folder = save_export(md_content, "markdown", topic, folder)
    exports["markdown"] = md_path
    
    # Email Newsletter
    newsletter_content = generate_email_newsletter(content, topic)
    nl_path, folder = save_export(newsletter_content, "newsletter", topic, folder)
    exports["newsletter"] = nl_path
    
    # README
    readme_content = generate_readme(topic, content, folder)
    readme_path, folder = save_export(readme_content, "readme", topic, folder)
    exports["readme"] = readme_path
    
    # Notion
    notion_content = export_to_notion_obsidian(content, topic, "notion")
    notion_path, folder = save_export(notion_content, "notion", topic, folder)
    exports["notion"] = notion_path
    
    # Obsidian
    obsidian_content = export_to_notion_obsidian(content, topic, "obsidian")
    obsidian_path, folder = save_export(obsidian_content, "obsidian", topic, folder)
    exports["obsidian"] = obsidian_path
    
    return exports, folder
